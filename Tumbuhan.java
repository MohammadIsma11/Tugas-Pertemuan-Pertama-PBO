public class Tumbuhan extends MakhlukHidup {
   String warnaDaun;
   String arahDaun;
   String habitat;
   String kebutuhan;
   String masa;
   String pembiakan;

    /**
     * @return the warnaDaun
     */
    public String getWarnaDaun() {
        return warnaDaun;
    }

    /**
     * @param warnaDaun the warnaDaun to set
     */
    public void setWarnaDaun(String warnaDaun) {
        this.warnaDaun = warnaDaun;
    }

    /**
     * @return the arahDaun
     */
    public String getArahDaun() {
        return arahDaun;
    }

    /**
     * @param arahDaun the arahDaun to set
     */
    public void setArahDaun(String arahDaun) {
        this.arahDaun = arahDaun;
    }

    /**
     * @return the habitat
     */
    public String getHabitat() {
        return habitat;
    }

    /**
     * @param habitat the habitat to set
     */
    public void setHabitat(String habitat) {
        this.habitat = habitat;
    }

    /**
     * @return the kebutuhan
     */
    public String getKebutuhan() {
        return kebutuhan;
    }

    /**
     * @param kebutuhan the kebutuhan to set
     */
    public void setKebutuhan(String kebutuhan) {
        this.kebutuhan = kebutuhan;
    }

    /**
     * @return the masa
     */
    public String getMasa() {
        return masa;
    }

    /**
     * @param masa the masa to set
     */
    public void setMasa(String masa) {
        this.masa = masa;
    }

    /**
     * @return the pembiakan
     */
    public String getPembiakan() {
        return pembiakan;
    }

    /**
     * @param pembiakan the pembiakan to set
     */
    public void setPembiakan(String pembiakan) {
        this.pembiakan = pembiakan;
    }

}
